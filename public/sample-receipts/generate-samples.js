// Simple script to create sample receipt images
const fs = require('fs');

// Create a simple 1x1 pixel PNG as placeholder
const createSimplePNG = (filename, color = [255, 255, 255]) => {
  // PNG header + IHDR + IDAT + IEND
  const pngData = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    0x00, 0x00, 0x00, 0x0D, // IHDR length
    0x49, 0x48, 0x44, 0x52, // IHDR
    0x00, 0x00, 0x00, 0x01, // width: 1
    0x00, 0x00, 0x00, 0x01, // height: 1
    0x08, 0x02, 0x00, 0x00, 0x00, // bit depth, color type, compression, filter, interlace
    0x90, 0x77, 0x53, 0xDE, // CRC
    0x00, 0x00, 0x00, 0x0C, // IDAT length
    0x49, 0x44, 0x41, 0x54, // IDAT
    0x08, 0x99, 0x01, 0x01, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01, // compressed data
    0x00, 0x00, 0x00, 0x00, // CRC
    0x49, 0x45, 0x4E, 0x44, // IEND
    0xAE, 0x42, 0x60, 0x82  // CRC
  ]);
  
  fs.writeFileSync(filename, pngData);
  console.log(`Created ${filename}`);
};

// Create sample receipts
createSimplePNG('fake-receipt.png');
createSimplePNG('restaurant-receipt.png');
createSimplePNG('gas-station-receipt.png');

console.log('Sample receipt images created!');
